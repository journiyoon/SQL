SELECT SUBSTRING(DATETIME,12,2) HOUR, COUNT(*) COUNT
FROM ANIMAL_OUTS
WHERE SUBSTRING(DATETIME,12,2) >= 9
GROUP BY SUBSTRING(DATETIME,12,2)
ORDER BY SUBSTRING(DATETIME,12,2) ASC;

